text = "Hello, World!"
print(text.find("o"))  # Output: 4 (returns the index of the first occurrence)
print(text.index("o"))  # Output: 4 (similar to find(), but raises an exception if not found)
print(text.count("l"))  # Output: 3 (counts the number of occurrences)
print(text.startswith("Hello"))  # Output: True (checks if it starts with "Hello")
