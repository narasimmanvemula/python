string = "Hello, World!"

print(string.upper())  # Output: HELLO, WORLD! (convert to uppercase)
print(string.lower())  # Output: hello, world! (convert to lowercase)
print(string.startswith("Hello"))  # Output: True (check if it starts with "Hello")
print(string.endswith("World!"))  # Output: True (check if it ends with "World!")
print(string.replace("Hello", "Hi"))  # Output: Hi, World! (replace "Hello" with "Hi")
print(string.split(", "))  # Output: ['Hello', 'World!'] (split into a list of substrings)
