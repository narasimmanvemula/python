string = "Hello, World!"

print(string[7:12])  # Output: World (slice a portion of the string)
print(string[:5])  # Output: Hello (slice from the beginning to index 5)
print(string[7:])  # Output: World! (slice from index 7 to the end)
print(string[::-1])  # Output: !dlroW ,olleH (reverse the string)
